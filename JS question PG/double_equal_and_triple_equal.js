// triple equal compares value and data types as well

console.log(1 =='1') //true 
console.log(1 === '1') // false 
console.log(null == null) // true 
console.log(null === null) // true 
console.log(null == undefined) // true (only use case of ==)
console.log(null === undefined) // false
console.log(1 != '1') // false
console.log(1 !== '1') // true 
